# Typecasting means conversion one datatype into another datatype

# two Type of Typecasting : 1.Explicit conversion (means i as a programmer do typecasting )
                        #   2.Implicit conversion ( means automatic python do typecasting)

# 1.Explicit conversion
a = "5" 
b = "8"
print(a + b) # output : 58
print(int(a) + int(b)) # output : 13

# 2.Implicit conversion
c = 8.8
d = 8
print(c + d) # output :16.8